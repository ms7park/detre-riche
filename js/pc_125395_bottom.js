/*     1730425 : 탭 버튼 이미지      */
jQuery(function($){
	var $obj = $('.img__1730425__');

	$obj.find('> a').on('click', function(){
		var $target = $(this).attr('href');

		$('.fac_tab_btn').removeClass('on');
		$(this).parent().addClass('on');
		$('.fac_tab_cont').hide();
		$($target).show();

		return false;
	});
});

